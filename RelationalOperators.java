package session03;

public class RelationalOperators {
    public static void main(String[] args) {
        //2) Relational operators - ( ==; !=; >; <, >= and <= ) ----- true or false

        int a = 100;
        int b = 200;

        System.out.println(a == b); //false
        System.out.println(a != b); //true
        System.out.println(a > b);//false
        System.out.println(a < b);//true
        System.out.println(a >= b);//false
        System.out.println(a <= b);//true
    }
}
